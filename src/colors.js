const HEX_RE = /^#?([0-9a-f]{3}|[0-9a-f]{6})$/i;

function normalizeHex(input) {
  let hex = input.replace(/^#/, "").toLowerCase();
  if (hex.length === 3) {
    hex = hex.split("").map((c) => c + c).join("");
  }
  return "#" + hex;
}

export class UnknownColorError extends Error {}

// Resolve a user-supplied name or hex string into a "#rrggbb" value.
export function resolveColor(input) {
  const value = String(input).trim();
  if (!value) {
    throw new UnknownColorError("No color given.");
  }
  if (HEX_RE.test(value)) {
    return { hex: normalizeHex(value), name: null };
  }
  const key = value.toLowerCase();
  if (key in THEMES) {
    return { hex: THEMES[key], name: key };
  }
  if (key in CSS_COLORS) {
    return { hex: CSS_COLORS[key], name: key };
  }
  throw new UnknownColorError(`Unknown color: "${value}"`);
}

export function randomPaletteColor() {
  const hex = PALETTE[Math.floor(Math.random() * PALETTE.length)];
  return { hex, name: null };
}

export function colorNames() {
  return {
    themes: Object.keys(THEMES),
    css: Object.keys(CSS_COLORS),
  };
}
